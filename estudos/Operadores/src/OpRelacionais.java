public class OpRelacionais {
public static void main (String[] argumentos) {
    double numero1 = 10;
    double numero2 = 11;

    // x é igual a y?
    System.out.println(numero1 == numero2); //false
    // x é diferente y?
    System.out.println(numero1 != numero2); //true
    // x é maior que y?
    System.out.println(numero1 > numero2); //false
    // x é menor que y?
    System.out.println(numero1 < numero2); //true
    // x é naior ou igual a y?
    System.out.println(numero1 >= numero2); //false
    // x é menor ou igual a y?
    System.out.println(numero1 <= numero2); //true
}
}